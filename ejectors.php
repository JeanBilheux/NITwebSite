					<p>
						The work began with measurements of DPFs at different soot and ash loadings. As the neutrons are able to penetrate the
						ceramic filter, they can take measurements of the hydrocarbon-rich particulates (soot) and metal-oxide-based ash.
						Although these materials are not necessarily highly sensitive to neutrons, they have a high surface area and
						are very hydroscopic (readily attracting moisture). The adsorbed water allows detection by neutrons. Neutron
						tomography is used to view and measure the thickness of the soot in the channels and the location of ash deposits.
						Ash, mostly from lubricant additives, affects engine efficiency by clogging the filter and increasing
						the backpressure on the engine and curtails filter life.
					</p>
					<p>
						Bilheux is also engaged with computational scientist Sreekanth Pannala and battery expert Jagjit Nanda
						at ORNL, as well as industry partners, to measure lithium transport inside complex electrodes.
					</p>
					<p>
						<q>This is an important research area for DOE and involves substantial participation by industrial partners,</q> Bilheux says. Battery
						storage devices have excellent energy and power-to-weight ratios and are now the power source of choice for
						cell phones, cameras, and notebook computers. Lithium batteries are also used extensively in biomedical and
						military applications, and prospects are good for the use of lithium batteries in transportation in the future.
					</p>
					<p>
						Bilheux is also working on multi-constrained jet flow with the United Technologies Research Center.
						Neutron imaging is being used to examine jet flow under different conditions to investigate new design
						capabilities for multiphase ejectors that can improve heating, ventilating, and air-conditioning system efficiency.
						There are currently no data on internal ejector flow to validate physical models needed to improve ejector design.
						Neutron imaging can provide these data.
					</p>
