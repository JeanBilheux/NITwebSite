					<h2>Scientist and Industry Use Neutron Imaging to Improve Energy Efficiency</h3>
					<p>
						Neutron scientists are partnering with industry to enhance engine and commercial cooling technologies in hopes of making
						improvements that will optimize fuel and energy efficiency.
					</p>
					<p>
						Hassina Bilheux, a physicist and the lead for developing ORNL's neutron imaging capabilities, uses cold neutrons at HFIR's
						CG-1D beam line to image automobile engine system components, two-phase fluid components in commercial cooling systems, and electrodes used for lithium batteries.
					</p>
					<div id="photo1" class="floating_image_right">
						<img src='publications/imaging_hassina.jpg' alt='Hassina at the beam line.' />
						<p>
							Instrument scientist Hassina Bilheux at CG-1D.
						</p>
					</div>
					<p>
						Neutron imaging is just taking off at ORNL, and although there aren't yet any instruments devoted exclusively to it, Bilheux and her colleagues have been
						using one of the cold neutron beam lines at HFIR for imaging work. <q>These are not imaging beam lines,</q> says Bilheux. <q>But we have been very successful
							since we started in December 2009 preparing CG-1D for imaging.</q>
					</p>
					<p>
						Bilheux hopes to eventually bring neutron imaging capabilities to SNS, where she can better and more cost-efficiently select the energy of the neutrons.
						SNS, she says, has a wide range of energies that makes it possible to image thick biological samples,
						which isn't possible at HFIR.
					</p>
					<blockquote>
						"We are truly pioneering a new field, and this is a unique time for all of us" - imaging scientist Hassina Bilheux
					</blockquote>
					<p>
						Progress is visible now in a series of industrial research projects already afoot or in the planning stage.
						Two of the projects under way involve vehicle technologies: producing two- and three-dimensional images
						of exhaust gas recirculation (EGR) coolers and images of diesel particulate filters (DPFs),
						which remove the black soot cloud so often associated with diesel exhaust. In both cases, the goal is to
						improve fuel efficiency and, in the case of the DPF project, to consider the emissions and materials impacts of the introduction of biofuels.
					</p>
					<p>
						To take measurements, researchers set a sample in the beam and use a detector behind it to collect the neutrons that
						are transmitted, literally taking shadow pictures of the sample. <q>Neutrons are especially great for engineering applications because
							they don't see metals very well but are efficient at seeing hydrogen-rich fluids,</q>Neutrons> Bilheux explains.
					</p>
					<p>
						In the EGR coolers project, researchers measured coolers from 10 participating companies.
					</p>
					<p>
						Neutron imaging measures how the hydrocarbon (enriched particulate matter) is deposited within an EGR cooler that shows significant clogging.
						The role of the coolers is to lower the oxygen content and the combustion temperatures, thereby reducing the formation of NOx (nitrogen oxides)
						in the cylinder. Thanks to the imaging, the researchers can measure the thickness and hydrocarbon content of the deposit and come to understand the
						spatial and time dynamics of particulate matter deposition. In future measurements, tomography will be used to image complete, intact coolers in three dimensions.
					</p>
					<p>
						A second vehicle project concerns how soot and ash build up in the DPF of a diesel-powered vehicle and the impact of fuel type.
						DPFs were developed to remove the particulates (primarily soot) once common in diesel engine exhaust. Bilheux is working with
						researchers at the Fuels, Engines, and Emissions Research Center (FEERC) at ORNL's National Transportation Research Center.
						At FEERC, the researchers use diesel engines operated on dynamometer platforms to introduce particulates to the DPF.
						This enables the study to occur on real engines under industry-relevant conditions; additionally, field-aged samples have
						been provided to the FEERC researchers from industrial partners.
					</p>
					<div id="photo2" class="floating_image_left">
